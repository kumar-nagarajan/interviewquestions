package javainterviewquestion1;

public class PrintNumberName {

	public static void main(String[] args) {
			int i = 10;
			switch(i) {
			case 1:
				System.out.println("One");
				break;
			case 2:
				System.out.println("Two");
				break;
			case 3:
				System.out.println("Three");
				break;
			default:
				System.out.println("Enter valid number");
				
			}
		
	}

}
